        /// <summary>
        /// Gets information from the data source for a user. Provides an option to update the last-activity date/time stamp for the user.
        /// </summary>
        /// <param name="username">The name of the user to get information for.</param>
        /// <param name="userIsOnline">true to update the last-activity date/time stamp for the user; false to return user information without updating the last-activity date/time stamp for the user.</param>
        /// <returns>
        /// A <see cref="T:System.Web.Security.MembershipUser"/> object populated with the specified user's information from the data source.
        /// </returns>
        public override MembershipUser GetUser(string username, bool userIsOnline)
        {
            UserService userService = new UserService();
            User user = userService.GetByUserName(username);

            if (user == null) return null;

            MembershipUser membership = new MembershipUser(
                this.Name,
                user.UserName,
                user.UserId,
                user.Email,
                string.Empty,
                string.Empty,
                true,
                false,
                user.DateCreated.HasValue?user.DateCreated.Value:DateTime.Now,
                user.UpdateDate.HasValue?user.UpdateDate.Value:DateTime.Now,
                DateTime.Now,
                DateTime.Now,
                DateTime.Now);

            return membership;
        }

        /// <summary>
        /// Gets user information from the data source based on the unique identifier for the membership user. Provides an option to update the last-activity date/time stamp for the user.
        /// </summary>
        /// <param name="providerUserKey">The unique identifier for the membership user to get information for.</param>
        /// <param name="userIsOnline">true to update the last-activity date/time stamp for the user; false to return user information without updating the last-activity date/time stamp for the user.</param>
        /// <returns>
        /// A <see cref="T:System.Web.Security.MembershipUser"/> object populated with the specified user's information from the data source.
        /// </returns>
        public override MembershipUser GetUser(object providerUserKey, bool userIsOnline)
        {
            UserService userService = new UserService();
            User user = userService.GetByUserId(Convert.ToInt32(providerUserKey));

            if (user == null) return null;

            MembershipUser membership = new MembershipUser(
                user.EmployeeName,
                user.UserName,
                user.UserId,
                user.Email,
                string.Empty,
                string.Empty,
                true,
                false,
                user.DateCreated.HasValue ? user.DateCreated.Value : DateTime.Now,
                user.UpdateDate.HasValue ? user.UpdateDate.Value : DateTime.Now,
                DateTime.Now,
                DateTime.Now,
                DateTime.Now);

            return membership;
        }

